class Basic_features:
    def __init__(self):

        # create an addition function
        def add(self, num1, num2):
            return num1 + num2
        
        # create a subtraction function
        def subtract(self, arg1, arg2):
            return arg1 - arg2
        
        # create a multiplication function
        def multiply(self, arg1, arg2):
            return arg1 * arg2
        
        # create a division function
        def divide(self, arg1, arg2):
            return arg1 / arg2